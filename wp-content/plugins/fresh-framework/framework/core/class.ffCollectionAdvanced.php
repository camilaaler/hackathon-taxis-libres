	public function callFunctionOnItems( $functionName ) {

		$functionArguments = func_get_args();
		$functionArgumentsClean = null;

		if( count( $functionArguments ) > 1 ) {
			$functionArgumentsClean = array_slice($functionArguments, 1);
		}

		foreach( $this as $oneItem ) {
			if( method_exists( $oneItem, $functionName ) ) {
				if( $functionArgumentsClean == null ) {
					call_user_func( array( $oneItem, $functionName ) );
				} else {
					call_user_func_array( array( $oneItem, $functionName), $functionArgumentsClean );
				}

			}
		}
	}

